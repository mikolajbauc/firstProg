public class OverridenMethods {

    int dodawaj(int a, int b){
        return a + b;
    }

    int dodawaj(int a, int b, int c){
        return a + b + c;
    }

    int dodawaj(int a, int b, int c, int d){
        return a + b + c + d;
    }

    int dodawaj(int a, int b, int c, int d, int e){
        return a + b + c + d + e;
    }
}
